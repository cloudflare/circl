type dhX448 func(dst, in, base *[x448.SizeKey]byte)
type dhX448Base func(dst, in *[x448.SizeKey]byte)

func base(t *testing.T, dhBase dhX448Base, dhFunc dhX448) {
	const times = 150
	var input, got, want x448.Key
	base := x448.GetBase()

	for j := 0; j < times; j++ {
		_, err := rand.Read(input[:])
		if err != nil {
			fmt.Println("rand error:", err)
			return
		}

		x, y := input, input
		for i := 0; i < times; i++ {
			dhFunc(&want, &x, &base)
			dhBase(&got, &y)
			x = want
			y = got
		}

		if got != want {
			t.Errorf("incorrect result: input %v got %v, want %v", input, got, want)
		}
	}
}

func rfc7748Times(t *testing.T, dhFunc dhX448) {
	for times, want := range testFoldTimes {
		var u, k [x448.SizeKey]byte
		u[0] = byte(x448.XCoordBase())
		k[0] = byte(x448.XCoordBase())
		if testing.Short() && times == uint32(1000000) {
			continue
		}
		var r [x448.SizeKey]byte
		for i := uint32(0); i < times; i++ {
			dhFunc(&r, &k, &u)
			u = k
			k = r
		}
		got := k
		if got != want {
			t.Errorf("incorrect result: got %v, want %v", got, want)
		}
	}
}

func rfc7748Kat(t *testing.T, dhFunc dhX448) {
	for _, v := range testVectorsRFC7748 {
		var got [x448.SizeKey]byte
		dhFunc(&got, &v.scalar, &v.input)
		if got != v.output {
			t.Errorf("incorrect result: got %v, want %v", got, v.output)
		}
	}
}

func TestX448_RFC7748(t *testing.T) {
	rfc7748Kat(t, x448.ScalarMult)
	rfc7748Times(t, x448.ScalarMult)
	base(t, x448.ScalarBaseMult, x448.ScalarMult)
}

